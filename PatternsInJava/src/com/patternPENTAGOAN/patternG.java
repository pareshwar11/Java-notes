package com.patternPENTAGOAN;

public class patternG {
	public static void main(String []args) {
		int n=5;
		for(int i=0;i<n;i++) {
			for(int j=0;j<=i;j++) {
			
					System.out.print("*");
					// i+j==n-1;
					//i=1+j=3=n-1 ( 5-1)
					//i=3+j=1(3+1)=n-1 (5-1)
				}
			System.out.println();
			}
	}

}
